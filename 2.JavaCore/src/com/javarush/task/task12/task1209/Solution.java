/* 
Три метода и минимум
*/

public class Solution {
    public static void main(String[] args) {
    }

    public static int min(int x, int y) {
        return x > y ? y : x;
    }

    public static long min(long x, long y) {
        return x > y ? y : x;
    }

    public static double min(double x, double y) {
        return x > y ? y : x;
    }
}
